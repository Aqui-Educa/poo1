def Soma(numeros):
    resultado = 0
    for numero in numeros:
        resultado += numero
    return resultado 

def Subtrai(numero1,numero2):
    resultado = numero1 - numero2
    return resultado

def Multiplica(numero1,numero2):
    resultado = numero1 * numero2
    return resultado

def Divide(numero1,numero2):
    resultado = numero1 / numero2
    return resultado
